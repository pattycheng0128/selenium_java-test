package Variable;

public class ch3_rectangle {

	public static void main(String[] args) {
		int width = 20;
		int height = 30;
		int area;// 面積
		int perimeter;// 周長
		area = width * height;
		perimeter = (width + height) * 2;
		System.out.println("矩形面積:" + area);
		System.out.println("矩形周長:" + perimeter);

	}

}
